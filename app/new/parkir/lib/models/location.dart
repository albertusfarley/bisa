class Location {
  final String name;
  final String category;
  final String address;
  final String call;
  final String thumbnail;
  final String url;
  final int open;
  final int closed;

  Location({required this.name, required this.category, required this.address, required this.call, required this.thumbnail, 
    required this.url, required this.open, required this.closed
  });

}